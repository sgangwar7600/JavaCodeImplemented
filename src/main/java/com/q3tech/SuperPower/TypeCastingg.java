package com.q3tech.SuperPower;

public class TypeCastingg {
	
	public static void main(String[] args) {
		
		int a = 10;  // 4 byte , implicit type casting, upcasting , 
		
		double b = a ;  // 8 byte
		
		System.out.println(b);
		
		
		double c = 10.0;  // 8 byte,  explicit type casting
		
		int d = (int)c;  // 4 byte
		
		System.out.println(d);
		
		
		
		
		
	}
}
